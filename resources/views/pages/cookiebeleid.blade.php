        <h2 class="text-xl font-black text-[#564D4A] mb-4 mt-10">1. Wat zijn cookies</h2>
        <p class="text-sm text-[#564D4A]/70 leading-relaxed mb-4">
            Cookies zijn kleine tekstbestanden die door je browser op je apparaat worden opgeslagen wanneer je een website bezoekt. Ze worden veel gebruikt om websites goed te laten functioneren, de gebruikerservaring te verbeteren en informatie te verstrekken aan de eigenaar van de website.
        </p>

        <h2 class="text-xl font-black text-[#564D4A] mb-4 mt-10">2. Welke cookies gebruiken wij</h2>
        <p class="text-sm text-[#564D4A]/70 leading-relaxed mb-4">
            BrainForge maakt gebruik van de volgende typen cookies:
        </p>

        <h3 class="text-base font-bold text-[#564D4A] mb-3 mt-6">Functionele cookies (noodzakelijk)</h3>
        <p class="text-sm text-[#564D4A]/70 leading-relaxed mb-4">
            Deze cookies zijn essentieel voor het functioneren van de website. Zonder deze cookies kan de website niet goed werken.
        </p>
        <ul class="list-disc list-inside text-sm text-[#564D4A]/70 leading-relaxed mb-4 space-y-1">
            <li><strong>Sessiecookie:</strong> houdt je inlogstatus bij zodat je niet op elke pagina opnieuw hoeft in te loggen. Verloopt bij het sluiten van de browser of na de ingestelde sessieduur.</li>
            <li><strong>CSRF-token:</strong> beschermt formulieren tegen ongeautoriseerde verzoeken. Verloopt bij het sluiten van de browser.</li>
            <li><strong>Cookievoorkeuren:</strong> onthoudt je cookievoorkeuren zodat we je niet herhaaldelijk hoeven te vragen. Verloopt na 12 maanden.</li>
        </ul>

        <h3 class="text-base font-bold text-[#564D4A] mb-3 mt-6">Analytische cookies</h3>
        <p class="text-sm text-[#564D4A]/70 leading-relaxed mb-4">
            Deze cookies helpen ons te begrijpen hoe bezoekers de website gebruiken. De verzamelde informatie is geanonimiseerd en wordt uitsluitend gebruikt om onze dienst te verbeteren.
        </p>
        <ul class="list-disc list-inside text-sm text-[#564D4A]/70 leading-relaxed mb-4 space-y-1">
            <li><strong>Paginabezoeken:</strong> we registreren welke pagina's worden bezocht om te begrijpen welke onderdelen populair zijn.</li>
            <li><strong>Prestatiemetingen:</strong> we meten laadtijden en prestaties om de snelheid van de website te optimaliseren.</li>
        </ul>
        <p class="text-sm text-[#564D4A]/70 leading-relaxed mb-4">
            We maken geen gebruik van tracking cookies van derden voor advertentiedoeleinden. BrainForge toont geen advertenties.
        </p>

