            <li><strong>Personalisatie:</strong> om je spelervaring te verbeteren en relevante content te tonen.</li>
            <li><strong>Communicatie:</strong> om je te informeren over belangrijke wijzigingen in de dienst of je account.</li>
            <li><strong>Beveiliging:</strong> om fraude te voorkomen en de veiligheid van het platform te waarborgen.</li>
